#include "libft.h"

int	ft_atoi(const char *str)
{
	long int	res;
	int			n;
	size_t		i;

	i = 0;
	n = 1;
	res = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
		n = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - 48);
		i++;
		if ((res * n) > 2147483647)
			return (-1);
		else if ((res * n) < -2147483648)
			return (0);
	}
	return (res * n);
}
